/**
 * 生成从今天开始的30天模拟数据
 */
export function generateMockFirstMonth(userId: string) {
  const today = new Date()

  return mockFirstMonthActions.map((action, index) => {
    const date = new Date(today)
    date.setDate(today.getDate() + index)
    const dateStr = date.toISOString().split('T')[0]

    return {
      user_id: userId,
      date: dateStr,
      ...action
    }
  })
}
